 # Este programa solicita un número del 1 al 7 y muestre el día de la semana correspondiente (1 es lunes, 7 es domingo).

num = int(input("Escribe un número del 1 al 7: "))

if num == 1:
  print("Lunes.")
if num == 2:
    print("Martes.")
if num == 3:
      print("Miercoles.")
if num == 4:
     print("Jueves.")
if num == 5: 
     print("Viernes.")
if num == 6:
    print("Sabado.")
if num == 7:
    print("Domingo.")
else:
   print("Error debe ingrsar un número del 1 al 7.")

